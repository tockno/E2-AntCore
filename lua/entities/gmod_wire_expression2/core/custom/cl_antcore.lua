-- Client entity scaling
net.Receive("antcore_editmodel", function(len)
	local entid = net.ReadInt(32)
	--local offset = net.ReadVector()
	local scale = net.ReadVector()
	local ang = net.ReadAngle()
	
	local ent = Entity(entid)
	
	if IsValid(ent) then
		local mat = Matrix()
		-- offset here
		mat:Scale(scale)
		mat:SetAngles(Angle(ang[1],ang[2],ang[3]))
		ent:EnableMatrix("RenderMultiply", mat)
	end
end)

-- Client receive clipboard text
net.Receive("antcore_clipboard_text", function(len)
	local text = net.ReadString()
	
	SetClipboardText(text)
end)